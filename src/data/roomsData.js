export const getRoomBySlug = (slug) => {
    return roomsData.find(room => room.slug === slug);
};

export const getRoomById = (id) => {
    return roomsData.find(room => room.id === parseInt(id));
};

export const getRoomsByCategory = (category) => {
    return roomsData.filter(room => room.category === category);
};

export const getRoomsByView = (view) => {
    return roomsData.filter(room => room.view === view);
};

